#include "push_swap.h"
#include <stdio.h>

int	main(int argc, char **argv)
{
	t_stack	a;
	t_stack	b;

	stack_init(&a);
	stack_init(&b);
	parse_args(argc, argv, &a);
	if (is_sorted(&a))
		return (0);
	index_stack(&a);
	if (stack_len(&a) <= 3)
		sort_three(&a);
	else
		sort_five(&a, &b);
	stack_clear(&a);
	stack_clear(&b);
	return (0);
}
